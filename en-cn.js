    function buildTrie(dict) {
        const root = {};
        for (const key in dict) {
            let node = root;
            const lower = key.toLowerCase();
            for (const ch of lower) {
                node[ch] ??= {};
                node = node[ch];
            }
            node.$ = dict[key];
        }
        return root;
    }

    const TRIE = buildTrie(DICT);

    function isWordChar(ch) {
        return /[a-zA-Z0-9]/.test(ch);
    }

    function translateByTrie(text) {
        const src = text;
        const lower = src.toLowerCase();
        let i = 0;
        let out = '';

        while (i < src.length) {
            const prev = i === 0 ? '' : src[i - 1];

            if (isWordChar(src[i]) && isWordChar(prev)) {
                out += src[i++];
                continue;
            }

            let node = TRIE;
            let j = i;
            let lastMatch = null;
            let lastIndex = i;

            while (j < src.length && node[lower[j]]) {
                node = node[lower[j]];
                j++;

                const next = src[j] || '';
                if (node.$ && !isWordChar(next)) {
                    lastMatch = node.$;
                    lastIndex = j;
                }
            }

            if (lastMatch) {
                out += lastMatch;
                i = lastIndex;
            } else {
                out += src[i++];
            }
        }
        return out;
    }

    function translateText(text) {
        if (!text || text.length < 2) return text;
        let result = applyRegexRules(text);
        result = translateByTrie(result);
        return result;
    }

    const translatedNodes = new WeakSet();

    function walk(node) {
        if (node.nodeType === Node.TEXT_NODE) {
            if (translatedNodes.has(node)) return;

            const original = node.nodeValue;
            const translated = translateText(original);

            if (original !== translated) {
                node.nodeValue = translated;
            }
            translatedNodes.add(node);
            return;
        }

        if (node.nodeType !== Node.ELEMENT_NODE) return;

        if (node.shadowRoot) {
            walk(node.shadowRoot);
        }

        for (const child of node.childNodes) {
            walk(child);
        }
    }

    const observer = new MutationObserver(mutations => {
        for (const m of mutations) {
            for (const node of m.addedNodes) {
                walk(node);
            }
        }
    });

    observer.observe(document.documentElement, {
        childList: true,
        subtree: true
    });

    walk(document.body);

